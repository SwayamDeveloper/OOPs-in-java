//Initialize object through reference variable

package Object;

public class Animal {
   String color;
   int age;
   
   public static void main(String[] args) {
	   Animal buzo = new Animal();
	   buzo.color="Black";
	   buzo.age=10;
   
	   System.out.println(buzo.color+" " + buzo.age );
	   
   }
	
}

